/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func flatten(root *TreeNode) {
	stack := []*TreeNode{root}
	for len(stack) > 0 {
		node := stack[len(stack)-1]
		stack = stack[:len(stack)-1]
		if node == nil {
			continue
		}
		if node.Left != nil {
			tmp := node.Right
			node.Right, node.Left = node.Left, nil
			dummy := node.Right
			for dummy.Right != nil {
				dummy = dummy.Right
			}
			dummy.Right = tmp
		}
		stack = append(stack, node.Right)
	}
}

